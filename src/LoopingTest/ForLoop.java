package LoopingTest;

public class ForLoop {

	public static void main(String[] args) {
		
		int i;
		for(i=1; i<=5; i++) {   //i=1  ; 1<=5; 1++
			                    //i=2  2<=5 2++
			                    //i=3  3<=5 3++
			                    //i=4  4<=5 4++
			                   // i=5  5<=5 5++ Stop
			System.out.println(i);
		}
 
	}

}
